						la actividad excesiva de la glándula, reducir o evitar un incremento excesivo de tamaño
						y en otros casos erradicar la presencia de células funcionales.</p>

						<p align="justify">Esta terapia es útil en casos de hipertiroidismo  secundario a enfermedad
